class _WordMyTabState extends State<WordMyTab> {
  List<WordItem> _words = [];
  bool _loading = false;

  @override
  void initState() {
    super.initState();
    _fetchWords();
  }

  Future<void> _fetchWords() async {
    setState(() => _loading = true);

    try {
      final prefs = await SharedPreferences.getInstance();
      final token = prefs.getString('jwt_token');
      final savedWordbookId = prefs.getInt('selectedWordbookId');

      if (token == null || token.isEmpty) {
        throw Exception('Î°úÍ∑∏Ïù∏Ïù¥ ÌïÑÏöîÌï©ÎãàÎã§.');
      }

      final url = Uri.parse(
          'http://localhost:8080/api/v1/wordbooks/$savedWordbookId/words');

      final response = await http.get(
        url,
        headers: {
          'Content-Type': 'application/json; charset=UTF-8',
          'Authorization': 'Bearer $token',
        },
      );

      if (response.statusCode == 200) {
        final data = jsonDecode(utf8.decode(response.bodyBytes)) as List;

        final loadedWords = data.map((e) {
          final wordEn = e['wordEn'] ?? '';
          final wordKrList = List<String>.from(e['wordKr'] ?? []);
          return WordItem(
            personalWordbookWordId: e['personalWordbookWordId'] ?? 0,
            word: wordEn,
            wordKr: wordKrList,
            favorite: e['favorite'] ?? false,
          );
        }).toList();

        setState(() => _words = loadedWords);
        print('‚úÖ Total words loaded: ${_words.length}');
      } else if (response.statusCode == 403) {
        throw Exception('Ï†ëÍ∑º Í∂åÌïúÏù¥ ÏóÜÏäµÎãàÎã§. Îã§Ïãú Î°úÍ∑∏Ïù∏ Ìï¥Ï£ºÏÑ∏Ïöî.');
      } else {
        throw Exception('Îã®Ïñ¥ Ï°∞Ìöå Ïã§Ìå® (${response.statusCode})');
      }
    } catch (e, st) {
      print('‚ùå Îã®Ïñ¥ Ï°∞Ìöå ÏóêÎü¨: $e');
      print(st);
    } finally {
      setState(() => _loading = false);
    }
  }

